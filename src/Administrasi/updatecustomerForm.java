    public void connect() {
            try {
            Class.forName("com.mysql.jdbc.Driver");
            con =DriverManager.getConnection("jdbc:mysql://localhost:3306/radhasankara","root","");
            st = con.createStatement();
            }
            catch(ClassNotFoundException | SQLException e) {
                JOptionPane.showMessageDialog(null,"Error conn");  
                System.out.println(e.getMessage());
            }
        }
    
    public void tampildata(){
        try{
            System.out.println(ID);
        rs = st.executeQuery("select * from tblcustomers where ID_customer = '"+ID+"'");
            while(rs.next()){
            String notelplengkap = rs.getString(8);
            String notelp = notelplengkap.substring(1);
            txtNama.setText(rs.getString(2));
            txtAlamat.setText(rs.getString(3));
            txtKelurahan.setText(rs.getString(4));
            txtKecamatan.setText(rs.getString(5));
            txtKota.setText(rs.getString(6));
            txtKodePos.setText(rs.getString(7));
            txtNomorTelepon.setText(notelp);
            txtEmail.setText(rs.getString(9));
            }
        }
        catch(Exception e){
                e.printStackTrace();
            }
    }
     
       
        
