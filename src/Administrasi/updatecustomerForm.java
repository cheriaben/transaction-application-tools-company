    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
        try{
            connect();
            String nama = txtNama.getText();
            String alamat = txtAlamat.getText();
            String kelurahan = txtKelurahan.getText();
            String kecamatan = txtKecamatan.getText();
            String kota = txtKota.getText();
            int kodepos = Integer.parseInt(txtKodePos.getText());
            String notelp = "+"+txtNomorTelepon.getText();
            String email = txtEmail.getText();
            if(email.contains("@")){
            st.executeUpdate("update tblcustomers set nama_customer = '"+nama+"', alamat = '"+alamat+"', kelurahan = '"+kelurahan+"', kecamatan = '"+kecamatan+"', kota = '"+kota+"', kodepos = "+kodepos+", no_telepon = '"+notelp+"', email = '"+email+"' where ID_customer = '"+ID+"'");
            JOptionPane.showMessageDialog(this,"Data customer berhasil diubah!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            dispose();
            customersdataForm customersdataform = new customersdataForm();
            }
            else{
                JOptionPane.showMessageDialog(this, "Email tidak sesuai!", "Gagal", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Data customer gagal diubah, silahkan coba lagi!", "Gagal", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnOkActionPerformed

