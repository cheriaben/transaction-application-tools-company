    private void btnresetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnresetActionPerformed
        // TODO add your handling code here:
        if(txtjwb.getText().equalsIgnoreCase(answer)){
         String pass = txtpassfill.getText();
         String cpass = txtpassfillc.getText();
         if(pass.equals(cpass)){
         try{
             st.executeUpdate("update tbllogin set password = '"+pass+"' where ID_account = '"+idacc+"'");
             JOptionPane.showMessageDialog(this,"Password berhasil diubah!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
             dispose();
         }
         catch(Exception e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Jawaban yang anda masukan salah, silahkan coba lagi!", "Gagal", JOptionPane.ERROR_MESSAGE);
            }
        }
         else{
             JOptionPane.showMessageDialog(this, "Konfirmasi password anda!", "Gagal", JOptionPane.ERROR_MESSAGE);
         }
        }
    }//GEN-LAST:event_btnresetActionPerformed

