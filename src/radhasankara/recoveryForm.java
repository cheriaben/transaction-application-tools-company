    private void btnprocessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnprocessActionPerformed
        // TODO add your handling code here:
        connect();
        try{
            uname = txtuname.getText();
            email = txtemail.getText();
            
            rs = st.executeQuery("select ID_account, recovery, answer from tbllogin where username ='"+uname+"' AND email = '"+email+"'");
            while(rs.next()){
                idacc = rs.getString(1);
                recover = rs.getString(2);
                answer = rs.getString(3);
            }
            if(recover==null){
                txtpassfill.setVisible(false);
                JOptionPane.showMessageDialog(this, "Username/email tidak ditemukan, silahkan coba lagi!", "Gagal", JOptionPane.ERROR_MESSAGE);
            }
            else{
                txtrecovery.setText(recover);
                txtpassfill.setVisible(true);
                jLabel6.setVisible(true);
                jLabel11.setVisible(true);
                txtpassfillc.setVisible(true);
                btnprocess.setVisible(true);
                btnreset.setVisible(true);
                btnprocess.setVisible(false);
                
                jLabel12.setVisible(true);
                txtrecovery.setVisible(true);
                jLabel13.setVisible(true);
                txtjwb.setVisible(true);
            }
            
            
        }
        catch (Exception e) {
          e.printStackTrace();
          JOptionPane.showMessageDialog(this, "Recovery password gagal, silahkan coba lagi!", "Gagal", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnprocessActionPerformed

